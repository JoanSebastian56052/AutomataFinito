    private void cambiaEstado(boolean a) {
        txtSymbol.setEditable(a);
        btnAddSymbol.setEnabled(a);
        txtStatusAuto.setEditable(a);
        btnAddStatus.setEnabled(a);
        btnFinish.setEnabled(a);
        chkStatusInitial.setEnabled(a);
    }
    private boolean existe1(String a, Vector s) {
        Iterator io = s.iterator();
        while(io.hasNext()) {
            String j = (String)io.next();
            if(j.equals(a)) {
                return true;
            }
        }
        return false;
    }
