    private void btnCreateAutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateAutoActionPerformed
        // TODO add your handling code here:
        nombre = txtName.getText();
        if(nombre.equals("")) {
            JOptionPane.showMessageDialog(rootPane, "Por favor ingrese un nombre para el automata", "Alerta!!", 2);
            txtName.requestFocus();
            return;
        }
        simbolos = new Vector();
        estados = new Vector();
        cambiaEstado(true);
        btnCreateAuto.setEnabled(false);
        txtName.setEditable(false);
        txtSymbol.requestFocus();
        chkStatusInitial.setEnabled(true);
        txtName.setEnabled(false);
    }//GEN-LAST:event_btnCreateAutoActionPerformed

    private void btnAddSymbolActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSymbolActionPerformed
        // TODO add your handling code here:
        if(!txtSymbol.getText().equals("")) {
            if(!existe1(txtSymbol.getText(), simbolos)) {
                n++;
                simbolos.add(txtSymbol.getText());
                txtViewSymbol.setText(txtViewSymbol.getText() + ", " + txtSymbol.getText());
                txtSymbol.setText("");
                txtSymbol.requestFocus();
            } else {
                JOptionPane.showMessageDialog(rootPane, "Ya Ingreso este simbolo", "ERROR !!!", 0);
                txtSymbol.setText("");
                txtSymbol.requestFocus();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar un caracter que represente el simbolo de entrada!", "ALERTA !!!", 2);
            txtSymbol.requestFocus();
        }
    }//GEN-LAST:event_btnAddSymbolActionPerformed

    private void btnAddStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddStatusActionPerformed
        // TODO add your handling code here:
        if(!txtStatusAuto.getText().equals("")) {
            m++;
            String est = "E" + m;
            boolean h = false;
            boolean f = false;
            
            if(chkStatusInitial.isSelected()) {
                h = true;
            }
            if(rbAceptation.isSelected()) {
                f = true;
            }
            Estado a = new Estado(est, txtStatusAuto.getText(), h, f);
            estados.add(a);
            
            if(h) {
                flag = true;
                if(f) {
                    txtViewStatus.setText(txtViewStatus.getText() + "\n" + "*" + est + " : " + txtStatusAuto.getText() + "(Aceptacion)");
                } else {
                    txtViewStatus.setText(txtViewStatus.getText() + "\n" + "*" + est + " : " + txtStatusAuto.getText());
                }
                chkStatusInitial.setEnabled(false);
            } else {
                if(f) {
                    txtViewStatus.setText(txtViewStatus.getText() + "\n" + est + " : " + txtStatusAuto.getText() + "(Aceptacion)");
                } else {
                    txtViewStatus.setText(txtViewStatus.getText() + "\n" + est + " : " + txtStatusAuto.getText());
                }
            }
            
            chkStatusInitial.setSelected(false);
            txtStatusAuto.selectAll();
            txtStatusAuto.requestFocus();
            h = false;
            f = false;
        } else {
            JOptionPane.showMessageDialog(rootPane, "Debe ingresar un nombre para el estado", "Alerta!!!", 2);
            txtStatusAuto.requestFocus();
        }
    }//GEN-LAST:event_btnAddStatusActionPerformed

    private void btnFinishActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinishActionPerformed
        // TODO add your handling code here:
        if(flag == true) {
            Vector a[][] = new Vector[m + 1][n + 2];
            for (int gg = 0; gg < m + 1; gg++) {
                for (int hh = 0; hh < n + 2; hh++) {
                    a[gg][hh] = new Vector();
                }
            }

            for (int i = 0; i < n; i++) {
                try {
                    a[0][i + 1].add(simbolos.get(i).toString());
                } catch (Exception p) {
                    a[0][i + 1] = new Vector();
                    a[0][i + 1].add(simbolos.get(i).toString());
                }
            }

            for (int i = 0; i < m; i++) {
                Estado e = (Estado) estados.get(i);
                try {
                    a[i + 1][0].add(e.getIcono());
                } catch (Exception p) {
                    a[i + 1][0] = new Vector();
                    a[i + 1][0].add(e.getIcono());
                }
            }
            a[0][n + 1] = new Vector();
            a[0][n + 1].add("Î»");

            simbolos.add("Î»");

            Automata aut = new Automata(nombre, simbolos, estados, a);
            IngresaAutomata ing = new IngresaAutomata(aut, index);
            ing.setVisible(true);
            this.setVisible(false);
            flag = false;
        }
    }//GEN-LAST:event_btnFinishActionPerformed

