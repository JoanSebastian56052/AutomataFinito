    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        btnFinish.setEnabled(true);
        Vector est = au.getEstados();
        Vector si = au.getSimbolos();
        int x = retornaPosEst(est, cbStatus.getSelectedItem().toString());
        int y = retornaPosSim(si, cbAdded.getSelectedItem().toString());
        if(!existe(trans[x][y], cbFinish.getSelectedItem().toString())) {
            try {
                trans[x][y].add(cbFinish.getSelectedItem().toString());
            } catch(Exception e) {
                trans[x][y] = new Vector();
                trans[x][y].add(cbFinish.getSelectedItem().toString());
            }
            modelo.setValueAt(trans[x][y], x, y);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Ya existe esta transicion", "Precaucion!!", 2);
        }
    }//GEN-LAST:event_btnAddActionPerformed

